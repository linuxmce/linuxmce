<?php
session_name('Web-admin');
session_start();

require('include/config/config.inc.php');
require('include/utils.inc.php');
require('include/template.class.inc.php');

$action=$_REQUEST['action'];

switch ($action){
	case 'setAutogeneratedScenario';
		$cgID=(int)$_REQUEST['cgID'];
		$disabled=($_REQUEST['lastValue']=='1')?0:1;
		$dbADO->Execute('UPDATE CommandGroup SET Disabled=? WHERE PK_CommandGroup=?',array($disabled,$cgID));
		
		header('Location: index.php?section='.$_REQUEST['backsection'].'&msg=Autogenerated scenario was updated.');
		exit();
	break;
	default:
		// do nothing, page is called without params
	break;
}
?>